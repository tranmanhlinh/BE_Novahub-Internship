
public class CompareToChallenge{    
  public static void main(String[] arg){     
     
      //5 example less than 0
      System.out.println("Apple".compareTo("Banana"));
      System.out.println("Highland Coffee".compareTo("Highland Teas"));
      System.out.println("Dau".compareTo("Dut"));
      System.out.println("My name".compareTo("Your name"));
      System.out.println("AMERICA".compareTo("america"));

      //5 example larger than 0
      System.out.println("Interest".compareTo("Hobby"));
      System.out.println("Things".compareTo("Stuff"));
      System.out.println("University".compareTo("College"));
      System.out.println("a".compareTo("Last Name"));
      System.out.println("Vietnam".compareTo("VIETNAM"));

      //2 examples equal to 0
      System.out.println("Linh".compareTo("Linh"));
      System.out.println("Tran".compareTo("Tran"));


     } //end main method
        

}//end class


